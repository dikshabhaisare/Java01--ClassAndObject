package OOPS.Constructor;
class Sample{
    int a;
    boolean b;
    double d;
}
public class DefaultConst1 {
    public static void main(String[] args) {
        Sample def = new Sample();
        System.out.println(def.a);
        System.out.println(def.b);
        System.out.println(def.d);
    }
}
